    name,
    location,
    course_type,
    par,
    holes,
    average_rating,
    review_count,
    green_fees_min,
    green_fees_max
FROM golf_courses 
WHERE state = 'GA' AND (city = 'Canton' OR city = 'Woodstock' OR city = 'Holly Springs' OR city = 'Ball Ground' OR city = 'Waleska' OR city = 'Jasper' OR city = 'Ellijay' OR city = 'Blue Ridge')
ORDER BY average_rating DESC, name;

-- Show summary
SELECT 
    'Canton Area Golf Courses Added Successfully' AS status,
    COUNT(*) AS total_courses,
    COUNT(CASE WHEN course_type = 'private' THEN 1 END) AS private_courses,
    COUNT(CASE WHEN course_type = 'public' THEN 1 END) AS public_courses,
    COUNT(CASE WHEN course_type = 'municipal' THEN 1 END) AS municipal_courses,
    COUNT(CASE WHEN is_featured = true THEN 1 END) AS featured_courses
FROM golf_courses 
WHERE state = 'GA' AND (city = 'Canton' OR city = 'Woodstock' OR city = 'Holly Springs' OR city = 'Ball Ground' OR city = 'Waleska' OR city = 'Jasper' OR city = 'Ellijay' OR city = 'Blue Ridge');
